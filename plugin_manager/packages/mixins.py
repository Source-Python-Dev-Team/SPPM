# App
from .models import Package


# =============================================================================
# >> ALL DECLARATION
# =============================================================================
__all__ = (
    'RetrievePackageMixin',
)


# =============================================================================
# >> MIX-INS
# =============================================================================
class RetrievePackageMixin(object):
    _package = None

    @property
    def package(self):
        if self._package is None:
            self._package = Package.objects.get(slug=self.kwargs['slug'])
        return self._package
