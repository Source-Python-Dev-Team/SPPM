# App
from .models import Plugin


# =============================================================================
# >> ALL DECLARATION
# =============================================================================
__all__ = (
    'RetrievePluginMixin',
)


# =============================================================================
# >> MIX-INS
# =============================================================================
class RetrievePluginMixin(object):
    _plugin = None

    @property
    def plugin(self):
        if self._plugin is None:
            self._plugin = Plugin.objects.get(slug=self.kwargs['slug'])
        return self._plugin
