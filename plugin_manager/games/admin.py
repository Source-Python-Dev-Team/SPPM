# =============================================================================
# >> IMPORTS
# =============================================================================
# Django
from django.contrib import admin

# App
from .models import Game


# =============================================================================
# >> ALL DECLARATION
# =============================================================================
__all__ = (
    'GameAdmin',
)


# =============================================================================
# >> ADMIN CLASSES
# =============================================================================
@admin.register(Game)
class GameAdmin(admin.ModelAdmin):
    exclude = (
        'slug',
    )
    list_display = (
        'name',
        'basename',
    )
    search_fields = (
        'name',
        'basename',
    )
