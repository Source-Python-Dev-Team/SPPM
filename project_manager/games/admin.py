"""Game admin classes."""

# =============================================================================
# >> IMPORTS
# =============================================================================
# Django
from django.contrib import admin

# App
from .models import Game


# =============================================================================
# >> ALL DECLARATION
# =============================================================================
__all__ = (
    'GameAdmin',
)


# =============================================================================
# >> ADMINS
# =============================================================================
@admin.register(Game)
class GameAdmin(admin.ModelAdmin):
    """Game admin."""

    exclude = (
        'slug',
    )
    list_display = (
        'name',
        'basename',
    )
    search_fields = (
        'name',
        'basename',
    )
