"""Tag validators."""

# =============================================================================
# >> IMPORTS
# =============================================================================
# Django
from django.core.validators import RegexValidator


# =============================================================================
# >> ALL DECLARATION
# =============================================================================
__all__ = (
    'tag_name_validator',
)


# =============================================================================
# >> GLOBAL VARIABLES
# =============================================================================
# Tags should:
#   Contain only lower-case characters.
